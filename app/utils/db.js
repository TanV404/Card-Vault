import postgres from 'postgres';

const connectionString = process.env.DATABASE_URL;

if (!connectionString) {
  throw new Error('DATABASE_URL environment variable is not defined.');
}

const globalForDb = global;

export const sql = globalForDb.sql || postgres(connectionString, {
  ssl: connectionString.includes('sslmode=require') || process.env.NODE_ENV === 'production' ? 'require' : false,
});

if (process.env.NODE_ENV !== 'production') {
  globalForDb.sql = sql;
}

let initialized = false;

export async function initDb() {
  if (initialized) return;

  // 1. Enable UUID extension
  try {
    await sql`create extension if not exists "uuid-ossp"`;
  } catch (e) {
    console.warn("Notice: Could not create extension 'uuid-ossp' (normal if privileges are restricted):", e.message);
  }

  // 2. Create users table
  try {
    await sql`
      create table if not exists users (
        id uuid primary key default gen_random_uuid(),
        email text unique not null,
        password_hash text,
        created_at timestamp with time zone default now() not null
      )
    `;
  } catch (e) {
    console.error("Error creating users table:", e.message);
  }

  // 3. Drop NOT NULL constraint on password_hash for existing tables
  try {
    await sql`
      alter table users alter column password_hash drop not null
    `;
  } catch (e) {
    console.warn("Notice: Could not alter users table (normal if column is already nullable or permissions are restricted):", e.message);
  }

  // 4. Create cards table
  try {
    await sql`
      create table if not exists cards (
        id bigint generated by default as identity primary key,
        user_id uuid references users(id) on delete cascade not null,
        person_name text,
        company_name text,
        designation text,
        email text,
        phone text,
        address text,
        created_at timestamp with time zone default now() not null
      )
    `;
  } catch (e) {
    console.error("Error creating cards table:", e.message);
  }

  // 5. Create index for optimization
  try {
    const indexExists = await sql`
      select 1 from pg_indexes where indexname = 'idx_cards_user_id'
    `;
    if (indexExists.length === 0) {
      await sql`
        create index idx_cards_user_id on cards(user_id)
      `;
    }
  } catch (e) {
    console.error("Error creating index idx_cards_user_id:", e.message);
  }

  initialized = true;
  console.log("Database schema checked/initialized successfully.");
}

export default sql;
