import postgres from 'postgres';

const connectionString = process.env.DATABASE_URL;

if (!connectionString) {
  throw new Error('DATABASE_URL environment variable is not defined.');
}

const globalForDb = global;

export const sql = globalForDb.sql || postgres(connectionString, {
  ssl: connectionString.includes('sslmode=require') || process.env.NODE_ENV === 'production' ? 'require' : false,
});

if (process.env.NODE_ENV !== 'production') {
  globalForDb.sql = sql;
}

let initialized = false;

export async function initDb() {
  if (initialized) return;
  try {
    // Enable UUID extension
    await sql`create extension if not exists "uuid-ossp"`;
    
    // Create users table
    await sql`
      create table if not exists users (
        id uuid primary key default gen_random_uuid(),
        email text unique not null,
        password_hash text not null,
        created_at timestamp with time zone default now() not null
      )
    `;

    // Create cards table
    await sql`
      create table if not exists cards (
        id bigint generated by default as identity primary key,
        user_id uuid references users(id) on delete cascade not null,
        person_name text,
        company_name text,
        designation text,
        email text,
        phone text,
        address text,
        created_at timestamp with time zone default now() not null
      )
    `;

    // Create indexes for optimization
    await sql`
      create index if not exists idx_cards_user_id on cards(user_id)
    `;

    initialized = true;
    console.log("Database schema checked/initialized successfully.");
  } catch (error) {
    console.error("Error initializing database schema:", error);
    // Do not re-throw, so app can boot, but log it
  }
}

export default sql;
